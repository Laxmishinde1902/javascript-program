console.log("==================Number datatype================");
var age = 22;
console.log("Age is:", age);
var ageTypeOf = typeof age;
console.log("Data type of age is : ", ageTypeOf);


console.log("================String Datatype==================");
var city = 'Mumbai';
console.log("City is :" , city);
var cityTypeOf = typeof city;
console.log("Type of City:" ,cityTypeOf);

var marks = '93';
console.log("Marks is :", marks);
var marksTypeOf = typeof marks;
console.log("Type of marks :" ,marksTypeOf);

console.log("=====================Boolean Datatype================")
var married = true;
console.log("Is Married:" , married);
var marriedTypeOf = typeof married;
console.log("Type of married:" , marriedTypeOf);

var failed = false;
console.log("Is Failed :" , failed);
var failedTypeOf = typeof failed;
console.log("Type of Failed :", failedTypeOf);

console.log("===============Special Datatype undefined=============");
var country ;
console.log("Country is :", country);
var countryTypeOf = typeof country;
console.log("Type of Country:",countryTypeOf);

console.log("=================Special Datatype Null=================");
var village = null;
console.log("village is :" , village);
var villageTypeOf = typeof village;
console.log("Type of village is:" , villageTypeOf);